/**
 * Extracts area code from a phone number and returns the mapped state abbreviation.
 * Supporting multiple formats: +19998887777, 19998887777, 9998887777, (999) 888-7777
 * 
 * @param {string} phone 
 * @returns {string|null} State abbreviation (e.g. "TX") or null if not found
 */
function getStateFromPhone(phone) {
  if (!phone || typeof phone !== 'string') return null;

  // Remove all non-numeric characters
  const digits = phone.replace(/\D/g, '');

  let areaCode = '';
  if (digits.length === 10) {
    areaCode = digits.substring(0, 3);
  } else if (digits.length === 11 && digits.startsWith('1')) {
    areaCode = digits.substring(1, 4);
  } else {
    // If it's not 10 or 11 digits, we might still try to take the first 3 if it looks like a local number
    // but usually area codes are the first 3 of a 10-digit sequence.
    return null;
  }

  return AREA_CODE_TO_STATE[areaCode] || null;
}

